    public void DataSach(){
        Services = new Services();
        defaultTableModel = new DefaultTableModel();
        
        tableOfContents.setModel(defaultTableModel);
        
        defaultTableModel.addColumn("Mã Tài Liệu");
        defaultTableModel.addColumn("Tên NXB");
        defaultTableModel.addColumn("Số Bản Phát Hành");
        defaultTableModel.addColumn("Tên Tác Gỉa");
        defaultTableModel.addColumn("Số Trang");
        
        List<sach> Sach = Services.getAllSach();
        
        for (sach i:Sach)
        {
            defaultTableModel.addRow(new Object[]{i.getMaTaiLieu(),i.getTenNXB(),i.getSoBanPhatHanh(),
                i.getTenTacGia(),i.getSoTrang()});
        }
    }
    
